#include "token.hpp"

namespace TabCompletion
{
//#####################################################################################################################
    Token::Token(std::string tok, TokenType type)
        : tok_{std::move(tok)}
        , type_{type}
    {
    }
//---------------------------------------------------------------------------------------------------------------------
    std::string Token::token() const
    {
        return tok_;
    }
//---------------------------------------------------------------------------------------------------------------------
    std::string const& Token::tokenHandle() const
    {
        return tok_;
    }
//---------------------------------------------------------------------------------------------------------------------
    TokenType Token::type() const
    {
        return type_;
    }
//#####################################################################################################################
}
